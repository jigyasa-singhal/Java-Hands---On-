public class StringsInJava  {
    public static void main(String[] args) {
        
        String name;
        name = new String ("Harry");
        System.out.println(name);

        String Name = "HArry";
        System.out.println(Name);

        int a =4;
        float b = 5.563566f;
        System.out.printf("The value of a is %d and the value of b is %f\n" , a,b);
        System.out.format("The value of a is %d and the value of b is %f\n" , a,b);
        System.out.printf("The value of a is %d and the value of b is %.2f\n" , a,b);
        System.out.printf("The value of a is %d and the value of b is %10.2f\n" , a,b);
        System.out.printf("The value of a is %d and the value of b is %5.2f" , a,b);






    }
}
